# Write a function named display_dict that takes a dictionary as argument\
# and prints it


def display_dict(dico):
    # For each key and associated val(ue) of item in items collection
    # of dictionary dico, print key and val on the same line
    for key, val in dico.items():
        print(key, "=>", val)


